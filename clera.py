tick_speed = .2

clear = "\033[H\033[J"  # ANSI escape code to clear the terminal

def animate_clera():
    for frame in frames:
        termW, termH = get_terminal_size()
        stdout.write(clear + "\n" * termH)
        for line in frame:
            stdout.write(" " * (int(termW / 2 - len(line) / 2)))
            print(line)
        sleep(tick_speed)
        
if __name__ == "__main__":
    try:
        animate_clera()
    except KeyboardInterrupt:
        exit(0)
